public class AMDShop {
    int freq;
    int cores;

    public AMDShop(int freq, int cores) {
        this.freq = freq;
        this.cores = cores;
    }

    public int getFreq() {
        return freq;
    }

    public void setFreq(int freq) {
        this.freq = freq;
    }

    public int getCores() {
        return cores;
    }

    public void setCores(int cores) {
        this.cores = cores;
    }
}
